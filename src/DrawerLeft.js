const DrawerHeader = styled("div")(({ theme }) => ({
  display: "flex",
  alignItems: "center",
  padding: theme.spacing(0, 1),
  // necessary for content to be below app bar
  ...theme.mixins.toolbar,
  justifyContent: "flex-end",
}));

export default function DrawerLeft(props) {
  const theme = useTheme();
  const [open, setOpen] = React.useState(false);
  const [currentPageName, setCurrentPageName] = React.useState("Home");
  const handleDrawerOpen = () => {
    setOpen(true);
  };

  const handleDrawerClose = () => {
    setOpen(false);
  };

  return (
    <Box sx={{ display: "flex" }}>
      <CssBaseline />
      <AppBar position="fixed" open={open}>
        <Toolbar>
          <IconButton
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            edge="start"
            sx={{ mr: 2, ...(open && { display: "none" }) }}
          >
            <MenuIcon />
          </IconButton>
          <Typography variant="h6" noWrap component="div">
            {currentPageName}
          </Typography>
        </Toolbar>
      </AppBar>
      <Drawer
        sx={{
          width: drawerWidth,
          flexShrink: 0,
          "& .MuiDrawer-paper": {
            width: drawerWidth,
            boxSizing: "border-box",
          },
        }}
        variant="persistent"
        anchor="left"
        open={open}
      >
        <DrawerHeader>
          <IconButton onClick={handleDrawerClose}>
            {theme.direction === "ltr" ? (
              <ChevronLeftIcon />
            ) : (
              <ChevronRightIcon />
            )}
          </IconButton>
        </DrawerHeader>
        <Divider />
        <List>
          {["Home", "Youtube", "Bilibili", "Convert"].map((text, index) => (
            <ListItem key={text} disablePadding>
              <ListItemButton onClick={() => setCurrentPageName(text)}>
                <ListItemIcon>
                  {(() => {
                    if (index === 0) {
                      return <HomeIcon />;
                    } else if (index === 1) {
                      return <YouTubeIcon />;
                    } else if (index === 2) {
                      return <BilibiliIcon />;
                    } else if (index === 3) {
                      return <VideoLibraryIcon />;
                    }
                  })()}
                </ListItemIcon>
                <ListItemText primary={text} />
              </ListItemButton>
            </ListItem>
          ))}
        </List>
        <Divider />
        <List>
          {["Account", "History", "Contact Us"].map((text, index) => (
            <ListItem key={text} disablePadding>
              <ListItemButton onClick={() => setCurrentPageName(text)}>
                <ListItemIcon>
                  {(() => {
                    if (index === 0) {
                      return <AccountCircleIcon />;
                    } else if (index === 1) {
                      return <HistoryIcon />;
                    } else if (index === 2) {
                      return <MailIcon />;
                    }
                  })()}
                </ListItemIcon>
                <ListItemText primary={text} />
              </ListItemButton>
            </ListItem>
          ))}
        </List>
      </Drawer>
      <Main open={open}>
        <DrawerHeader />
        {props.children}
      </Main>
    </Box>
  );
}
